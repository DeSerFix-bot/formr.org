<p>
	The first tab "Settings" lets you set a couple of simple general pieces of information about your study
</p>
<p>
	The "CSS" tab allows you to apply custom styles to every page of your study. If you want to limit styles to
	certain pages, you can use CSS classes referring to either position in the run (e.g. <code class="css">.run_position_10 {}</code>) or module type (e.g. <code class="css">.run_unit_type_Survey {}</code>). Learn about <a href="http://docs.webplatform.org/wiki/guides/getting_started_with_css">CSS at Webplatform.org</a>.
</p>
<p>
	The "JS" tab allows you to apply custom scripts to every page of your study. This is a fully-fledged programming language. You can use it to make things move, give dynamic hints to the user and so on. Learn about <a href="http://www.codecademy.com/tracks/javascript">JS at Codecademy.com</a>.
</p>